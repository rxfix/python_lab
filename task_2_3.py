    item = massage[idx]  # элемент списка
    sign = ''  # знак числа
    if item[:1] == '+' or item[:1] == '-':  # если первый символ знак
        sign = item[0]  # сохраняем этот знак
        item = item[1:]  # и работаем только с оставшеюся частью строки

