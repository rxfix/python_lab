# Формирование из обработанного списка строки:
idx = 0
while idx < len(massage):
    item = massage[idx]  # элемент списка
    if item[:1] == '+' or item[:1] == '-':  # если первый символ знак
        item = item[1:]  # работаем только с оставшеюся частью строки

    item_is_num = True  # проверяем все ли символы цифры
    for char in range(len(item)):
        if '0' <= item[char] <= '9':
            _item_is_num = True
        else:
            _item_is_num = False
        item_is_num = item_is_num and _item_is_num

    if item_is_num:  # если все символы цифры
        massage[idx] = massage[idx - 1] + massage[idx] + massage[idx + 1]  # копируем соседние кавычки с к числу
        massage.pop(idx + 1)  # удаляем кавычки обособливающие число
        massage.pop(idx - 1)
    idx += 1
massage_str = ' '.join(massage)  # формируем из обработанного списка строку
